CREATE DEFINER=`root`@`localhost` PROCEDURE `insert_product_to_cart`(order_id_p INT, product_p INT)
BEGIN
	DECLARE product_exists TINYINT DEFAULT FALSE;
	DECLARE p_exists TINYINT DEFAULT FALSE;
    DECLARE p_price DECIMAL(10, 2);
    
    SELECT COUNT(*) INTO product_exists FROM product WHERE product_id = product_p;
    
	IF product_exists = FALSE THEN
		SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = "Product Not Found!";
	END IF;
    
    SELECT current_price INTO p_price FROM product WHERE product_id = product_p;
    
    SELECT COUNT(*) INTO p_exists FROM order_product WHERE order_id = order_id_p AND product_id = product_p;
	
    IF p_exists = FALSE THEN
		INSERT INTO order_product VALUES(order_id_p, product_p, 1, p_price);
	ELSE
		UPDATE order_product SET quantity = quantity + 1 WHERE order_id = order_id_p AND product_id = product_p;
	END IF;
END ;;
